SELECT
    PRO.AD_IDEXTERNO,
    --PRO.CODGRUPOPROD,
    PRO.CODVOL,
    PRO.DESCRPROD,
    PRO.TEMIPICOMPRA,
    PRO.TEMIPIVENDA,
    PRO.UNIDADE,
    PRO.USACODBARRASQTD,
    PRO.USOPROD,
    PRO.MARCA,
    PRO.NCM,
    PRO.REFERENCIA,
    PRO.REFFORN

FROM TGFPRO PRO

INNER JOIN TGFGRU GRU ON PRO.CODGRUPOPROD = GRU.CODGRUPOPROD
LEFT JOIN AD_CABCPRO ABC ON PRO.CODPROD = ABC.CODPROD

WHERE
    PRO.REFERENCIA <> '0'
    AND SUBSTR(PRO.CODGRUPOPROD, 0, 2) NOT IN (11, 12, 13, 14)
    AND PRO.ATIVO = 'S'
    --AND SUBSTR(PRO.REFERENCIA, 0, 3) = 'FRO' 

ORDER BY PRO.CODPROD
